package day3_classes;

//Math logic
public class MathE {

    public static double plusE(double numOne, double numTwo) {
        return numOne + numTwo;
    }

    public static double minusE(double numOne, double numTwo) {
        return numOne - numTwo;
    }

    public static double timesE(double numOne, double numTwo) {
        return numOne * numTwo;
    }

    public static double divideE(double numOne, double numTwo) {
        return numOne / numTwo;
    }

}
